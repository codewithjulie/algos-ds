#include <stdio.h>

int main(void)
{
    int A[6] = {31, 41, 59, 26, 41, 58};
    int key, i, j;
    
    for (i = 1; i < 6; i++)
    {
        key = A[i];
        j = i - 1;
        while (j >= 0 && A[j] > key)
        {
            A[j + 1] = A[j];
            j = j - 1;
        }
        A[j + 1] = key;
    }
    for (int i = 0; i < 6; i++)
    {
        printf("%i\t", A[i]);
    } 
    
}

